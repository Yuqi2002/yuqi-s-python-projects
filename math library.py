# math library
# strings

import math

# rounding functions

# ciel rounds up
a =math.ceil(3.1)
print(a)

# floor rounds down
a=math.floor(3.1)
print(a)

# regular rounding 
a=round(3.1)
print(a)

# trig
degrees=math.pi
print(math.sin(degrees))

# square root
a=math.sqrt(25)
print(a)

# exponents
a=math.pow(3,3)
print(a)

# absolute value
a=abs(-5)
print(a)

